package com.bbpay.wap;
public class EpayHeader {
    private String key;
    private String value;
    public EpayHeader() {}
    public String getKey() {
        return key;
    }
    public String getValue() {
        return value;
    }
    public void setKey(String s) {
        key = s;
    }
    public void setValue(String s) {
        value = s;
    }
}
